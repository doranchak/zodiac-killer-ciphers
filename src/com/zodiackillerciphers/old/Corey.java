	static boolean isAlpha(char ch) {
		char up = Character.toUpperCase(ch);
		for (int i=0; i<alphabet.length; i++) if (alphabet[i].equals(""+up)) return true;
		return false;
	}
	
	static int diff(char a, char b) {
		int pos = -1000;
		for (int i=0; i<alphabet.length; i++) {
			if (alphabet[i].equals(""+a)) {
				pos = i; 
				break;
			}
		}
		if (pos == -1000) return -1000;
		
		int d=0; boolean backwards = false;
		while(true) {
			if (alphabet[pos].equals(""+b)) {
				if (Math.abs(d-26) < d)
					return d-26;
				return d;
			}
			d++;
			pos++;
			pos %= 26;
		}
		
	}
	
