	public static void makePlotData() {
		int num = 1;
		
		int xMax = 0; 
		int yMin = 0;
		for (int[] row : data) {
			int[] rect = rectangleFrom(row);
			System.out.println("set object " + num + " rect from " + rect[0]+","+rect[1] + " to " + rect[2]+","+rect[3] + " fc lt 2");
			xMax = Math.max(xMax, rect[0]);
			xMax = Math.max(xMax, rect[2]);
			yMin = Math.min(yMin, rect[1]);
			yMin = Math.min(yMin, rect[3]);
			num++;
		}
		System.out.println("xMax " + xMax + " yMin " + yMin);
	}
	
	/** compute median distance to next row, to try to locate gaps */
	public static void gaps() {
		for (int row = 0; row < 19; row++) {
			int[] distances = new int[17]; // 17 columns
			for (int col = 0; col < 17; col++) {
				int y1 = data[17*row+col][1];
				int y2 = data[17*(row+1)+col][1];
				distances[col] = y2-y1;
			}
			Arrays.sort(distances);
			//for (int dd : distances) d += dd + " "; 
			String d = distances[8]+" "+(row+1)+" "+(row+2);
			System.out.println(d);
		}
	}
	
	/** compute median "drift" for each row (symbols seem to drift downward along each row) */
	public static void drift() {
		for (int row = 0; row < 20; row++) {
			int[] distances = new int[16];
			int sum = 0;
			for (int col = 0; col < 16; col++) {
				int y1 = data[17*row+col][1];
				int y2 = data[17*row+col+1][1];
				distances[col] = y2-y1;
				sum += distances[col]; 
			}
			Arrays.sort(distances);
			//for (int dd : distances) d += dd + " "; 
			String d = distances[8]+" "+sum+" "+(row+1);
			System.out.println(d);
		}
	}
	
	public static int[] rectangleFrom(int[] row) {
		return new int[] {row[0], 0-row[1], row[0] + row[2], 0-row[1]-row[3]};
	}
	
